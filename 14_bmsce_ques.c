// Write a program to calculate factorial of a number
#include<stdio.h>
int fact(int *n){
    if(*n == 1 || *n == 0){
        return 1;
    }
    else{
        return (*n) * fact(n-1);
    }
}
int main(){
    int n;
    printf("Please enter any number: \n");
    scanf("%n",n);
    printf("Factorial of %d is %d\n", n,fact(&n));
    
    return 0;
}